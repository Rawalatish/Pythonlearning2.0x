# Pop will remove/delete the index value


squares = [1, 4, 9, 16, 25]


print(squares.pop(0))
print(squares)                # [4, 9, 16, 25]

a = "12"
c = 12.34
b = int(a)                    # here by using int we convert the string into int
d = int(c)                    # here by using int we convert the float into int

print(type(a))               # o/p = <class 'str'>
print(type(c))               # o/p = <class 'float'>
print(type(b))               # o/p = <class 'int'>
print(type(d))               # o/p = <class 'int'>

