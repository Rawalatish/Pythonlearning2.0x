# Loop
#  Loop is repeat a block of code multiple times

for i in range(1, 10):  # if dont define the range then it will start from 0
    print("Hello world")
    print(i)

print("--------")

for i in range(5):  # if dont define the range then it will start from 0

    print(i)  # 0,1,2,3,4

    print("----")

for i in range(4, 8):
    print(i)  # 4,5,6,7

    print("------")

for i in range(1, 10, 2):  # giving the  op in terms of 2 steps difference like 1,3,5,7
    print(i)  # op> 1,3,5,7,9

# range (start, stop, step)
